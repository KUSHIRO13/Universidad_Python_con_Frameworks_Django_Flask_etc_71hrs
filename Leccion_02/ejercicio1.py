# Ejercicio 1: Rectangulo

# Entrada de datos
alto = int(input("Proporciona el alto del rectangulo:"))
ancho = int(input("Proporciona el ancho del rectangulo:"))

# Operacion
Area = alto * ancho
Perimetro = (alto + ancho) * 2

# Salida de datos
print(f"Area: {Area}")
print(f"Perimetro: {Perimetro}")