a = 2
b = 2

igual = a == b
print(f"El resultado de igualar es {igual}")

diferente = a != b
print(f"El resultado de diferente es {diferente}")

mayorQue = a > b
print(f"El resultado de mayor que es {mayorQue}")

mayorOIgualQue = a >= b
print(f"El resultado de mayor o igual que es {mayorOIgualQue}")

menorQue = a < b
print(f"El resultado de menor que es {menorQue}")

menorOIgualQue = a <= b
print(f"El resultado de menor o igual que es {menorOIgualQue}")
